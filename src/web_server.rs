/// Maximum encoded bytes retained per stream for browser fast-start. If one GOP
/// exceeds this bound, caching pauses until the next keyframe rather than
/// retaining an undecodable delta suffix.
const VIDEO_FAST_START_MAX_BYTES: usize = video::MAX_VIDEO_FRAME_LEN;

static NEXT_UPLOAD_TEMP_ID: AtomicU64 = AtomicU64::new(1);

/// The latest complete keyframe-led GOP forwarded to local web clients.
/// `bytes` charges [`SharedVideoFrame::retained_bytes`], the allocation each
/// cached frame actually keeps alive.
#[derive(Default)]
struct VideoFastStart {
    frames: VecDeque<SharedVideoFrame>,
    bytes: usize,
    /// Delta frames dropped since the cache last held a keyframe, for
    /// diagnostics: a nonzero count means a play click in that window replayed
    /// nothing and the browser had to wait for the next keyframe.
    dropped_without_key: u64,
}

impl VideoFastStart {
    fn push(&mut self, stream_id: u32, frame: SharedVideoFrame, is_key: bool) {
        if is_key {
            if self.dropped_without_key > 0 {
                kvlog::info!(
                    "video fast start resumed at keyframe",
                    stream_id,
                    dropped_frames = self.dropped_without_key
                );
            }
            self.dropped_without_key = 0;
            self.frames.clear();
            self.bytes = 0;
        } else if self.frames.is_empty() {
            self.dropped_without_key += 1;
            if self.dropped_without_key == 1 {
                kvlog::warn!(
                    "video fast start has no keyframe, dropping deltas until the next one",
                    stream_id
                );
            }
            return;
        }

        if self.bytes.saturating_add(frame.retained_bytes()) > VIDEO_FAST_START_MAX_BYTES {
            kvlog::warn!(
                "video fast start cache overflowed, pausing until the next keyframe",
                stream_id,
                cached_frames = self.frames.len(),
                cached_bytes = self.bytes,
                frame_bytes = frame.len()
            );
            self.frames.clear();
            self.bytes = 0;
            return;
        }
        self.bytes += frame.retained_bytes();
        self.frames.push_back(frame);
    }
}

/// Reads only the routing fields from an inner video frame after validating its
/// declared length. The encoded payload remains untouched.
fn video_frame_meta(frame: &[u8]) -> Option<(u32, bool)> {
    if frame.len() < video::VIDEO_FRAME_HEADER_LEN {
        return None;
    }
    let size = u32::from_le_bytes(frame[0..4].try_into().ok()?) as usize;
    if size < video::VIDEO_FRAME_HEADER_LEN
        || size > video::MAX_VIDEO_FRAME_LEN
        || size != frame.len()
    {
        return None;
    }
    let is_key = match frame[12] {
        0 => false,
        1 => true,
        _ => return None,
    };
    let stream_id = u32::from_le_bytes(frame[13..17].try_into().ok()?);
    Some((stream_id, is_key))
}

/// A single chat entry the frontend renders.
///
/// The body is split into [`Fragment`]s (prose and highlighted code) that the
/// browser composes directly. The wire form is the binary encoding in
/// [`crate::web_wire`], not JSON.
#[derive(Clone)]
pub struct WebMessage {
    pub id: u64,
    pub sender: String,
    /// The original plaintext body, used to populate the browser composer when
    /// the local author edits a text message. It is never inserted as HTML.
    pub body: String,
    /// Whether this message was authored by the client hosting the web view.
    pub local: bool,
    /// Whether the displayed body is the folded result of an edit mutation.
    pub edited: bool,
    pub timestamp_ms: u64,
    pub attachment: Option<WebAttachment>,
    /// `Some` for a file message. The feed upserts by this id together with
    /// `timestamp_ms`, because transfer ids are reused after a server restart.
    pub file_id: Option<u64>,
    /// The chat message id, distinct from [`id`](Self::id), which collapses to
    /// the transfer id for file messages. Paired with `timestamp_ms` it is the
    /// durable key message references target; `0` when unknown (a bare inline
    /// file with no announcement).
    pub message_id: u64,
    /// The precomputed `@@` reference code for this message (without the
    /// prefix), so the browser can copy or quote a reference without a codec.
    /// Empty when the message is not referenceable.
    pub ref_code: String,
    /// The body pre-split into prose and highlighted code fragments.
    pub fragments: Vec<Fragment>,
}

/// An inline media file attached to a [`WebMessage`], served from `/files`.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct WebAttachment {
    /// The served file name. The frontend builds the URL as `/files/<name>`.
    pub name: String,
    /// One of `image`, `video`, `audio`, or `file`.
    pub kind: String,
    /// Intrinsic pixel width, set for `image` attachments whose header parsed.
    ///
    /// The frontend reserves the box from `width`/`height` so a decoding image
    /// never grows the layout. `None` for non-images or an unreadable header.
    pub width: Option<u32>,
    /// Intrinsic pixel height, paired with [`width`](WebAttachment::width).
    pub height: Option<u32>,
}

impl WebAttachment {
    pub fn from_served_file(served_name: &str, dimensions: Option<(u32, u32)>) -> Self {
        let kind = classify(served_name);
        let (width, height) = match (kind, dimensions) {
            ("image", Some((w, h))) => (Some(w), Some(h)),
            _ => (None, None),
        };
        Self {
            kind: kind.to_string(),
            name: served_name.to_string(),
            width,
            height,
        }
    }
}

/// The `@@` code identifying `message`, precomputed for the browser.
fn chat_ref_code(message: &ChatMessage) -> String {
    rpc::msgref::MessageRef {
        room_id: message.room_id,
        message_id: message.message_id,
    }
    .encode()
}

impl WebMessage {
    /// Builds a message from a relayed chat message, resolving any `@@`
    /// references in its body through `resolver`.
    pub fn from_chat(
        message: &ChatMessage,
        resolver: web_wire::RefResolver,
        local_user: Option<UserId>,
    ) -> Self {
        let file_id = message.file_transfer_id.map(|transfer_id| transfer_id.0);
        WebMessage {
            // A file announcement keys on the transfer id so it shares an id with
            // the inline file message that later enriches it.
            id: file_id.unwrap_or(message.message_id.0),
            sender: message.sender_name.clone(),
            body: message.body.clone(),
            local: Some(message.sender) == local_user,
            edited: message.flags.edited(),
            fragments: split_fragments(&message.body, resolver),
            timestamp_ms: message.timestamp_ms,
            attachment: None,
            file_id,
            message_id: message.message_id.0,
            ref_code: chat_ref_code(message),
        }
    }
}

impl WebMessage {
    /// Builds a message representing a received file, with an inline attachment.
    ///
    /// `served_name` is the file's actual name on disk under the receive
    /// directory, which is what `/files/<name>` resolves. It can differ from
    /// `metadata.file_name` when a name collision was renamed on save.
    ///
    /// `dimensions`, when set, is the intrinsic pixel size of an image. It is
    /// recorded only for the `image` kind so the frontend can reserve the box.
    pub fn from_file(
        metadata: &FileMetadata,
        served_name: &str,
        dimensions: Option<(u32, u32)>,
        local_user: Option<UserId>,
    ) -> Self {
        // Name the body after the served file, not the sender's original name, so
        // the recipient sees where the file actually landed after any save-time
        // rename. Mirrors the announcement format so the size metadata survives
        // whichever of the two messages wins the upsert merge.
        let body = file_announcement_body(served_name, metadata.size);
        WebMessage {
            id: metadata.transfer_id.0,
            sender: metadata.sender_name.clone(),
            body: body.clone(),
            local: Some(metadata.sender) == local_user,
            edited: false,
            fragments: split_fragments(&body, &|_| None),
            timestamp_ms: metadata.timestamp_ms,
            attachment: Some(WebAttachment::from_served_file(served_name, dimensions)),
            file_id: Some(metadata.transfer_id.0),
            // The announcement message carries the identity; merge_from keeps it.
            message_id: 0,
            ref_code: String::new(),
        }
    }
}

impl WebMessage {
    /// Builds a file message from persisted history: the chat announcement plus
    /// the stored served name and image dimensions. Used to populate the web
    /// backlog with a room's file messages when the room is entered.
    pub fn from_history_file(
        message: &ChatMessage,
        served_name: &str,
        size: u64,
        dimensions: Option<(u32, u32)>,
        local_user: Option<UserId>,
    ) -> Self {
        let file_id = message.file_transfer_id.map(|transfer_id| transfer_id.0);
        // Rebuild the body from the served name rather than reusing the stored
        // announcement, which carries the sender's original name; the live view
        // does the same in `from_file` so a reload reads identically.
        let body = file_announcement_body(served_name, size);
        WebMessage {
            id: file_id.unwrap_or(message.message_id.0),
            sender: message.sender_name.clone(),
            body: body.clone(),
            local: Some(message.sender) == local_user,
            edited: message.flags.edited(),
            fragments: split_fragments(&body, &|_| None),
            timestamp_ms: message.timestamp_ms,
            attachment: Some(WebAttachment::from_served_file(served_name, dimensions)),
            file_id,
            message_id: message.message_id.0,
            ref_code: chat_ref_code(message),
        }
    }
}

impl WebMessage {
    /// Folds a later message for the same file into this one. Order-independent:
    /// the incoming fields win, but an attachment is never dropped, so the inline
    /// version's media survives whether it arrives before or after the
    /// announcement placeholder. The reference identity survives the same way:
    /// only the announcement message knows it, the inline file does not.
    fn merge_from(&mut self, incoming: WebMessage) {
        let attachment = incoming.attachment.or_else(|| self.attachment.take());
        let message_id = if incoming.message_id != 0 {
            incoming.message_id
        } else {
            self.message_id
        };
        let ref_code = if incoming.ref_code.is_empty() {
            std::mem::take(&mut self.ref_code)
        } else {
            incoming.ref_code.clone()
        };
        *self = WebMessage {
            attachment,
            message_id,
            ref_code,
            ..incoming
        };
    }
}

