#[allow(clippy::too_many_arguments)]
fn log_http_request_end(
    id: HttpRequestId,
    method: Option<HttpMethod>,
    path: Option<&str>,
    phase: HttpRequestPhase,
    request_bytes: usize,
    response_bytes: u64,
    elapsed: Duration,
    end: &HttpRequestEnd,
) {
    let method = method.map_or("UNKNOWN", HttpMethod::as_str);
    let path = path.unwrap_or("");
    let elapsed_us = u64::try_from(elapsed.as_micros()).unwrap_or(u64::MAX);
    match end {
        HttpRequestEnd::Complete { status, keep_alive } => kvlog::info!(
            "web http request finished",
            request_id = id.get(),
            method,
            path,
            phase = phase.as_str(),
            outcome = "complete",
            status = *status,
            keep_alive = *keep_alive,
            request_bytes,
            response_bytes,
            elapsed_us
        ),
        HttpRequestEnd::ClientEof if request_bytes == 0 => {
            kvlog::debug!(
                "web http connection closed before request",
                request_id = id.get(),
                phase = phase.as_str(),
                outcome = "client_eof",
                elapsed_us
            );
        }
        HttpRequestEnd::ClientEof => kvlog::warn!(
            "web http request aborted",
            request_id = id.get(),
            method,
            path,
            phase = phase.as_str(),
            outcome = "client_eof",
            request_bytes,
            response_bytes,
            elapsed_us
        ),
        HttpRequestEnd::ReadError { error } => kvlog::warn!(
            "web http request aborted",
            request_id = id.get(),
            method,
            path,
            phase = phase.as_str(),
            outcome = "read_error",
            error = error.as_str(),
            request_bytes,
            response_bytes,
            elapsed_us
        ),
        HttpRequestEnd::WriteError { error } => kvlog::warn!(
            "web http request aborted",
            request_id = id.get(),
            method,
            path,
            phase = phase.as_str(),
            outcome = "write_error",
            error = error.as_str(),
            request_bytes,
            response_bytes,
            elapsed_us
        ),
        HttpRequestEnd::Timeout => kvlog::warn!(
            "web http request timed out",
            request_id = id.get(),
            method,
            path,
            phase = phase.as_str(),
            outcome = "timeout",
            request_bytes,
            response_bytes,
            elapsed_us
        ),
        HttpRequestEnd::InternalError => kvlog::warn!(
            "web http request aborted",
            request_id = id.get(),
            method,
            path,
            phase = phase.as_str(),
            outcome = "internal_error",
            request_bytes,
            response_bytes,
            elapsed_us
        ),
    }
}

fn log_websocket_close(
    id: WebSocketId,
    reason: WebSocketCloseReason,
    remaining_clients: usize,
    abandoned_uploads: u32,
) {
    match reason {
        WebSocketCloseReason::ClientEof => kvlog::info!(
            "websocket closed",
            ws_id = id.get(),
            reason = "client_eof",
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::ClientClose { code } => kvlog::info!(
            "websocket closed",
            ws_id = id.get(),
            reason = "client_close",
            close_code = code.unwrap_or(0),
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::Protocol { code, detail } => kvlog::warn!(
            "websocket closed",
            ws_id = id.get(),
            reason = "protocol",
            close_code = code,
            detail,
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::InvalidUtf8 => kvlog::warn!(
            "websocket closed",
            ws_id = id.get(),
            reason = "invalid_utf8",
            close_code = 1007,
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::ReadError => kvlog::warn!(
            "websocket closed",
            ws_id = id.get(),
            reason = "read_error",
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::WriteError => kvlog::warn!(
            "websocket closed",
            ws_id = id.get(),
            reason = "write_error",
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::Timeout => kvlog::warn!(
            "websocket closed",
            ws_id = id.get(),
            reason = "timeout",
            remaining_clients,
            abandoned_uploads
        ),
        WebSocketCloseReason::ConnectionDropped => kvlog::warn!(
            "websocket closed",
            ws_id = id.get(),
            reason = "connection_dropped",
            remaining_clients,
            abandoned_uploads
        ),
    }
}

/// An in-progress browser upload: the temp file its chunk frames stream into.
struct UploadSink {
    upload_id: u32,
    name: String,
    expected_size: u64,
    received: u64,
    path: PathBuf,
    file: fs::File,
}

impl UploadSink {
    /// Creates the temp file for `upload_id` under a dedicated web-uploads
    /// directory, named from a sanitized `name` so the served file keeps a
    /// recognizable extension.
    fn create(id: WebSocketId, upload_id: u32, name: &str, expected_size: u64) -> io::Result<Self> {
        let dir = std::env::temp_dir().join("chatt-web-uploads");
        fs::create_dir_all(&dir)?;
        let name = sanitize_upload_name(name);
        let temp_id = NEXT_UPLOAD_TEMP_ID.fetch_add(1, Ordering::Relaxed);
        let path = dir.join(format!(
            "{}-{temp_id}-{}-{upload_id}-{name}",
            std::process::id(),
            id.get()
        ));
        let file = fs::File::create(&path)?;
        Ok(Self {
            upload_id,
            name,
            expected_size,
            received: 0,
            path,
            file,
        })
    }

    /// Appends one chunk's bytes to the temp file.
    fn append(&mut self, bytes: &[u8]) -> io::Result<()> {
        use std::io::Write;
        let received = self
            .received
            .checked_add(bytes.len() as u64)
            .ok_or_else(|| io::Error::new(io::ErrorKind::InvalidData, "upload size overflow"))?;
        if received > self.expected_size {
            return Err(io::Error::new(
                io::ErrorKind::InvalidData,
                "upload exceeds declared size",
            ));
        }
        self.file.write_all(bytes)?;
        self.received = received;
        Ok(())
    }

    fn finish(mut self) -> io::Result<PathBuf> {
        use std::io::Write;
        self.file.flush()?;
        drop(self.file);
        Ok(self.path)
    }
}

/// Reduces an uploaded file name to a flat, safe base name. Path separators and
/// parent references are stripped so the temp file never escapes its directory.
fn sanitize_upload_name(name: &str) -> String {
    let base = name.rsplit(['/', '\\']).next().unwrap_or(name);
    let trimmed = base.trim_matches('.');
    if trimmed.is_empty() {
        "upload".to_string()
    } else {
        trimmed.to_string()
    }
}

