    fn read_http_headers(stream: &mut TcpStream) -> String {
        let mut out = Vec::new();
        let mut byte = [0u8; 1];
        while !out.ends_with(b"\r\n\r\n") {
            stream.read_exact(&mut byte).unwrap();
            out.push(byte[0]);
        }
        String::from_utf8(out).unwrap()
    }

    fn read_ws_frame(stream: &mut TcpStream) -> (u8, Vec<u8>) {
        let mut head = [0u8; 2];
        stream.read_exact(&mut head).unwrap();
        let opcode = head[0] & 0x0f;
        let mut len = (head[1] & 0x7f) as usize;
        if len == 126 {
            let mut ext = [0u8; 2];
            stream.read_exact(&mut ext).unwrap();
            len = u16::from_be_bytes(ext) as usize;
        } else if len == 127 {
            let mut ext = [0u8; 8];
            stream.read_exact(&mut ext).unwrap();
            len = u64::from_be_bytes(ext) as usize;
        }
        let mut payload = vec![0u8; len];
        stream.read_exact(&mut payload).unwrap();
        (opcode, payload)
    }

    #[test]
    fn live_message_broadcasts_to_connected_client() {
        let cfg = WebConfig {
            enabled: true,
            readonly: true,
            bind: "127.0.0.1:0".to_string(),
            ..WebConfig::default()
        };
        let (web_tx, _web_rx) = mpsc::channel();
        let sender = spawn(
            &cfg,
            DownloadStore::new(64 * 1024 * 1024),
            100,
            web_tx,
            true,
        )
        .unwrap();

        let mut stream = TcpStream::connect(sender.local_addr()).unwrap();
        stream
            .set_read_timeout(Some(Duration::from_secs(2)))
            .unwrap();
        stream
            .write_all(
                b"GET /ws HTTP/1.1\r\n\
Host: localhost\r\n\
Upgrade: websocket\r\n\
Connection: Upgrade\r\n\
Sec-WebSocket-Key: x3JJHMbDL1EzLkh9GBhXDw==\r\n\
Sec-WebSocket-Version: 13\r\n\
\r\n",
            )
            .unwrap();

        let headers = read_http_headers(&mut stream);
        assert!(
            headers.starts_with("HTTP/1.1 101 Switching Protocols"),
            "{headers}"
        );

        // The feed frames are binary (opcode 0x2). The first is an empty sync.
        let (opcode, payload) = read_ws_frame(&mut stream);
        assert_eq!(opcode, 0x2);
        let sync = web_wire::decode_window(&payload);
        assert_eq!(sync.kind, web_wire::KIND_SYNC);
        assert!(sync.messages.is_empty());

        // The config envelope follows the sync frame as a text frame.
        let (opcode, _) = read_ws_frame(&mut stream);
        assert_eq!(opcode, 0x1);

        sender.send(WebMessage::text_for_test(1, "hello web"));

        let (opcode, payload) = read_ws_frame(&mut stream);
        assert_eq!(opcode, 0x2);
        let message = web_wire::decode_single(&payload);
        assert_eq!(message.fragments, vec![Fragment::text("<p>hello web</p>")]);
    }

    /// Sends a client-to-server text frame, which RFC 6455 requires to be masked.
    fn write_ws_text(stream: &mut TcpStream, text: &str) {
        write_ws_frame(stream, true, 0x1, text.as_bytes());
    }

    /// Sends a client-to-server binary frame, masked as RFC 6455 requires.
    fn write_ws_binary(stream: &mut TcpStream, payload: &[u8]) {
        write_ws_frame(stream, true, 0x2, payload);
    }

    fn write_ws_frame(stream: &mut TcpStream, fin: bool, opcode: u8, payload: &[u8]) {
        assert!(
            payload.len() < 126,
            "test payloads stay in the short length"
        );
        let mask = [0x37u8, 0xfa, 0x21, 0x3d];
        let mut frame = vec![
            if fin { 0x80 | opcode } else { opcode },
            0x80 | payload.len() as u8,
        ];
        frame.extend_from_slice(&mask);
        for (i, byte) in payload.iter().enumerate() {
            frame.push(byte ^ mask[i % 4]);
        }
        stream.write_all(&frame).unwrap();
    }

    /// Consumes the config envelope that follows the sync frame on every connect.
    fn drain_config(stream: &mut TcpStream) {
        let (opcode, config) = read_ws_frame(stream);
        assert_eq!(opcode, 0x1);
        assert!(String::from_utf8(config).unwrap().contains("\"config\""));
    }

    /// Opens a browser feed, draining the sync frame and the config envelope that
    /// always follow a connect, and returns the socket ready for live frames.
    fn open_ready_ws(addr: impl std::net::ToSocketAddrs) -> TcpStream {
        let mut stream = open_ws(addr);
        let (_, sync) = read_ws_frame(&mut stream);
        assert_eq!(web_wire::decode_window(&sync).kind, web_wire::KIND_SYNC);
        drain_config(&mut stream);
        stream
    }

    fn open_ws(addr: impl std::net::ToSocketAddrs) -> TcpStream {
        open_ws_with_origin(addr, None)
    }

    fn open_ws_with_origin(addr: impl std::net::ToSocketAddrs, origin: Option<&str>) -> TcpStream {
        let mut stream = TcpStream::connect(addr).unwrap();
        stream
            .set_read_timeout(Some(Duration::from_secs(2)))
            .unwrap();
        let origin = origin.map_or_else(String::new, |origin| format!("Origin: {origin}\r\n"));
        stream
            .write_all(
                format!(
                    "GET /ws HTTP/1.1\r\n\
Host: localhost\r\n\
Upgrade: websocket\r\n\
Connection: Upgrade\r\n\
Sec-WebSocket-Key: x3JJHMbDL1EzLkh9GBhXDw==\r\n\
Sec-WebSocket-Version: 13\r\n\
{origin}\r\n"
                )
                .as_bytes(),
            )
            .unwrap();
        let headers = read_http_headers(&mut stream);
        assert!(
            headers.starts_with("HTTP/1.1 101 Switching Protocols"),
            "{headers}"
        );
        stream
    }

